def create_folder_white_noise(sigma, n, seed):
    folder_name = f"white_noise_{sigma}_{n}_{seed}_folder"
    os.makedirs(folder_name, exist_ok=True)
    return folder_name

def move_file_to_folder(filename, folder):
    if not os.path.exists(folder):
        print(f"Folder '{folder}' does not exist. Creating it now.")
        os.makedirs(folder)

    if os.path.exists(filename):
        shutil.move(filename, folder)
        print(f"Moved '{filename}' to '{folder}'.")
    else:
        print(f"File '{filename}' does not exist.")

# Function to simulate White Noise process
def simulate_white_noise(sigma, n, seed):
    np.random.seed(seed)  # For reproducibility
    # White noise is just a sequence of random samples from a normal distribution
    noise = np.random.normal(0, sigma, n)
    return noise

def add_date_column(file_name, freq='s'):
    df = pd.read_csv(file_name)
    n_samples = len(df)
    df['date'] = pd.date_range(start='2000-01-01', periods=n_samples, freq=freq)
    df.to_csv(file_name, index=False)
    print(f"Updated {file_name} with date column.")

def white_noise_create_folder_simulate_split_save_to_csv(sigma, n, seed, freq='s'):
  folder = create_folder_white_noise(sigma, n, seed)
  white_noise_series = simulate_white_noise(sigma, n, seed)
  train_size = int((2/3)*len(white_noise_series))
  white_noise_series_train = white_noise_series[:train_size]
  white_noise_series_test = white_noise_series[train_size:]

  df_train = pd.DataFrame({'value': white_noise_series_train})
  df_test = pd.DataFrame({'value': white_noise_series_test})
  df = pd.DataFrame({'value': white_noise_series})

  df_train.to_csv(f"{folder}/white_noise_{sigma}_{n}_{seed}_train.csv", index=False)
  df_test.to_csv(f"{folder}/white_noise_{sigma}_{n}_{seed}_test.csv", index=False)
  df.to_csv(f"{folder}/white_noise_{sigma}_{n}_{seed}_data.csv", index=False)

  add_date_column(f"{folder}/white_noise_{sigma}_{n}_{seed}_train.csv", freq='s')
  add_date_column(f"{folder}/white_noise_{sigma}_{n}_{seed}_test.csv", freq='s')
  add_date_column(f"{folder}/white_noise_{sigma}_{n}_{seed}_data.csv", freq='s')

