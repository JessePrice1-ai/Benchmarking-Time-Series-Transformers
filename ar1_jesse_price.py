# prompt: generate ar1 192 one-step ahead forecasts given the fact that  true_data = true_data[start_idx-1: start_idx + testing_sample_size-1] is the data to get the one step ahead forecasts from  and take true_data, start_idx,testing_sample_size as arguemnts and return the simulated data
def get_ar1_forecasts(true_data, start_idx, testing_sample_size, phi):
    """
    Generates 192 one-step ahead forecasts for AR(1) model.

    Args:
        true_data (array-like): The time series data.
        start_idx (int): The starting index for forecasting.
        testing_sample_size (int): The number of samples for testing.

    Returns:
        array-like: The simulated data (forecasts).
    """

    # Extract the relevant portion of the true data
    true_data = true_data[start_idx - 1: start_idx + testing_sample_size - 1]
    # Initialize simulated data with the first value of the test data
    simulated_data = [phi * value for value in true_data]

    return np.array(simulated_data[:])

def integer_always_round_down(value):
    return int(value // 1)

def ar1_true_v_fed_plot(phi, sigma, n, seed, modes, seq_len, label_len, pred_len, e_layers, d_layers, factor,
    enc_in, dec_in, c_out, d_model, train_epochs, seed_fedformer, old_name, new_name):
    import numpy as np
    import scipy.stats as stats

    pred_folder = (f'ar1_{phi}_{sigma}_{n}_{seed}_folder/ar1_{modes}_{seq_len}_{label_len}_{pred_len}_{e_layers}_{d_layers}_{factor}_{enc_in}_{dec_in}_{c_out}_{d_model}_{train_epochs}_{seed_fedformer}_{old_name}_{new_name}/pred.npy')
    true_folder = f'ar1_{phi}_{sigma}_{n}_{seed}_folder/ar1_{phi}_{sigma}_{n}_{seed}_test.csv'

    # Load prediction and true data
    pred_data = np.load(pred_folder)
    pred_data = pred_data.reshape(-1)

    true_df = pd.read_csv(true_folder)
    true_data = true_df.iloc[:,0].values

    # Compute testing sample size
    testing_sample_size = integer_always_round_down((n * (1/3) * 0.2) // 32) * 32
    start_idx = int(n * (1/3) - n * (1/3) * 0.2)
    true_data_compare = true_data[start_idx: start_idx + testing_sample_size]

    ar1_theoretical = get_ar1_forecasts(true_data, start_idx, testing_sample_size, phi)
    df = pd.DataFrame(ar1_theoretical)
    df.to_csv(f'ar1_{phi}_{sigma}_{n}_{seed}_folder/ar1_{modes}_{seq_len}_{label_len}_{pred_len}_{e_layers}_{d_layers}_{factor}_{enc_in}_{dec_in}_{c_out}_{d_model}_{train_epochs}_{seed_fedformer}_{old_name}_{new_name}/ar1_theoretical' + ".csv", index=False)

    mse_real_v_fed = mean_squared_error(true_data_compare, pred_data)
    print(f"Mean Squared Error of Fedformer Forecasts: {mse_real_v_fed:.6f}")

    mse_real_v_theoretical = mean_squared_error(true_data_compare, ar1_theoretical)
    print(f"Mean Squared Error of Optimal ARIMA Forecasts: {mse_real_v_theoretical:.6f}")

    print(f"Ratio of Mean Squared Error of Optimal ARIMA Forecasts to Mean Squared Error of FedFormer Forecasts: {mse_real_v_theoretical/mse_real_v_fed}")

    print("Ljung Box: FedFormer Forecast Errors")
    error_true_fed = true_data_compare - pred_data
    ljung_box_result = sm.stats.acorr_ljungbox(error_true_fed, lags=10)
    print(ljung_box_result)

    print("Ljung Box: ARIMA Forecast Errors")
    error_true_theoretical = true_data_compare - ar1_theoretical
    ljung_box_result = sm.stats.acorr_ljungbox(error_true_theoretical, lags=10)
    print(ljung_box_result)

    # Creating a DataFrame
    data = pd.DataFrame({
        'Error Type': ['FED'] * len(error_true_fed) + ['Theoretical'] * len(error_true_theoretical),
        'Error Value': np.concatenate([error_true_fed, error_true_theoretical])
    })

    # Plotting
    plt.figure(figsize=(8, 5))
    sns.boxplot(x='Error Type', y='Error Value', data=data, palette='Set2')
    plt.title('Boxplots of Forecast Errors for FedFormer and Optimal ARIMA Models')
    plt.xlabel('Model')
    plt.ylabel('Error')
    plt.grid(True)
    plt.show()

    # Creating a DataFrame
    data = pd.DataFrame({
        'Error Type': ['FED'] * len(error_true_fed) + ['Theoretical'] * len(error_true_theoretical),
        'Error Value': np.concatenate([np.abs(error_true_fed), np.abs(error_true_theoretical)])
    })

    # Plotting
    plt.figure(figsize=(8, 5))
    sns.boxplot(x='Error Type', y='Error Value', data=data, palette='Set2')
    plt.title('Boxplots of Absolute Forecast Errors for FedFormer and Optimal ARIMA Models')
    plt.xlabel('Model')
    plt.ylabel('Error')
    plt.grid(True)
    plt.show()

    plt.figure(figsize=(8, 6))
    sns.kdeplot(error_true_fed, label="FedFormer Forecast Errors", shade=True)
    sns.kdeplot(error_true_theoretical, label="Optimal ARIMA Forecast Errors", shade=True)
    plt.title("Error Distribution Comparison")
    plt.xlabel("Error Value")
    plt.ylabel("Density")
    plt.legend()
    plt.show()

    import numpy as np

    # Compute the difference in absolute errors: positive means FedFormer is worse
    diff = np.abs(error_true_fed) - np.abs(error_true_theoretical)

    # One-sample t-test: tests if mean(diff) is significantly different from 0 (two-sided)
    t_stat, p_value_two_sided = stats.ttest_1samp(diff, 0)

    # Convert to one-sided p-value (testing if theoretical is better, i.e., FedFormer errors > theoretical errors)
    if t_stat > 0:
        p_value_one_sided = p_value_two_sided / 2
    else:
        p_value_one_sided = 1 - (p_value_two_sided / 2)

    print(f"One-sided t-test p-value (Theoretical < FedFormer): {p_value_one_sided:.6f}")

    # Print results
    print(f"Mean of Difference Between Fedformer Forecasts and Optimal ARIMA Forecasts: {np.mean(diff)}")
    print(f"T-Statistic: {t_stat}")
    print(f"P-Value One Sided: {p_value_one_sided}")
    print(f'Standard Deviation of the Difference Between Fedformer Forecasts and Optimal ARIMA Forecasts: {np.std(diff)}')

    fig, axes = plt.subplots(5, 2, figsize=(12, 8))

    # Plot ACF and PACF for pred_data
    plot_acf(pred_data, lags=20, ax=axes[0, 0], zero=False)
    axes[0, 0].set_title('ACF for pred_data')

    plot_pacf(pred_data, lags=20, ax=axes[0, 1], zero=False)
    axes[0, 1].set_title('PACF for pred_data')

    plot_acf(ar1_theoretical, lags=20, ax=axes[1,0], zero=False)
    axes[1,0].set_title("ACF for ar1_theoretical")

    plot_pacf(ar1_theoretical, lags=20, ax=axes[1,1], zero=False)
    axes[1,1].set_title("PACF for ar1_theoretical")

    plot_acf(error_true_fed, lags=20, ax=axes[2,0], zero=False)
    axes[2,0].set_title("ACF for error_true_fed")

    plot_pacf(error_true_fed, lags=20, ax=axes[2,1], zero=False)
    axes[2,1].set_title("PACF for error_true_fed")

    plot_acf(error_true_theoretical, lags=20, ax=axes[3,0], zero=False)
    axes[3,0].set_title("ACF for error_true_theoretical")

    plot_pacf(error_true_theoretical, lags=20, ax=axes[3,1], zero=False)
    axes[3,1].set_title("PACF for error_true_theoretical")

    plot_acf(true_data_compare, lags=20, ax=axes[4,0], zero=False)
    axes[4,0].set_title("ACF for true_data_compare")

    plot_pacf(true_data_compare, lags=20, ax=axes[4,1], zero=False)
    axes[4,1].set_title("PACF for true_data_compare")

    # Adjust layout and show the plot
    plt.tight_layout()
    plt.show()

    # Assuming true_data_compare and pred_data are NumPy arrays of the same size
    X = sm.add_constant(pred_data)  # Adding intercept term
    y = true_data_compare

    # Fit Ordinary Least Squares (OLS) regression
    model = sm.OLS(y, X).fit()
    print("TRUE v. FEDFORMER")
    print(model.summary())

    # Plot the regression line
    plt.figure(figsize=(8, 6))
    plt.scatter(pred_data, true_data_compare, label="Data", color='blue', alpha=0.6)
    plt.plot(pred_data, model.fittedvalues, color='red', label="Regression Line")
    plt.xlabel("Fedformer AR(1) Values")
    plt.ylabel("True Data Values")
    plt.title("Regression Line: TRUE vs. FEDFormer")
    plt.legend()
    plt.show()

    # Assuming true_data_compare and pred_data are NumPy arrays of the same size
    X = sm.add_constant(ar1_theoretical)  # Adding intercept term
    y = true_data_compare

    # Fit Ordinary Least Squares (OLS) regression
    model = sm.OLS(y, X).fit()
    print("TRUE v. THEORETICAL")
    print(model.summary())

    # Plot the regression line
    plt.figure(figsize=(8, 6))
