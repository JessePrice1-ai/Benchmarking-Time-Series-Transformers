# Function to simulate AR(1) process
def simulate_ar1(phi, sigma, n, seed):
    np.random.seed(seed)  # For reproducibility
    noise = np.random.normal(0, sigma, n)
    series = np.zeros(n)
    for t in range(1, n):
        series[t] = phi * series[t-1] + noise[t]
    return series

